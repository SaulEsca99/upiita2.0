            <p>En el año de 1986, la Dirección General del IPN propuso la creación de la Unidad Profesional Interdisciplinaria en Ingeniería y Tecnologías Avanzadas 
                (UPIITA). Este proyecto fue supervisado por la Secretaría Académica y la Dirección de Estudios Profesionales en Ingeniería y Ciencias Físico Matemáticas 
                en coordinación con la Secretaría Superior de Cónyork (ESCOM) del Instituto Politécnico Nacional.</p>
            
            <p>Conforme al Órgano Informativo Judicial del Instituto Politécnico Nacional "Gaceta Politécnica" número 389 de 15 de julio de 1997, 
                se publicó el ACUERDO del H. Consejo General Consultivo del IPN del día 27 de junio del mismo año, por el que se dispone de la creación de la 
                Unidad Profesional Interdisciplinaria en Ingeniería y Tecnologías Avanzadas (UPIITA), entrando en vigor al día siguiente de su publicación 
                (16 de julio de 1997), con sus tres carreras: Ingeniería Biónica, Ingeniería Mecatrónica e Ingeniería Telemática, carreras que se van pagando y aún nivel de estudios, 
                han sido de gran interés para las nuevas generaciones de estudiantes que egresan del nivel medio superior en el país.</p>
            
            <p>Se creó, así, una Unidad con carácter interdisciplinario para el establecimiento de opciones educativas de calidad, a nivel superior y posgrado, 
                en el ámbito de las tecnologías avanzadas; como respuesta a la tendencia mundial hacia la competitividad y globalización, 
                sumada al vertiginoso avance de la ciencia y la tecnología en todas las áreas del saber humano y su impacto en el sector industrial.</p>
            
            <p>La UPIITA inició sus labores académicas a partir del primer periodo febrero 1965-1997, y desde entonces recibió a 236 alumnos en el primer semestre. 
                En la segunda generación, se tuvo un ingreso de 207 alumnos, en la tercera de 262 alumnos y en la cuarta de 338 alumnos; en el 2009 se recibió a 450 alumnos. 
                Para el arranque de sus labores que corresponden a la primera generación, se utilizaron algunos salones del edificio de laboratorios de la ESCOM. 
                A partir de febrero de 1997, con la existencia de dos edificios de aulas se trasladó a las instalaciones que actualmente ocupa siendo construcciones con 
                características modernas como alumbrado automático, piso flotante en los espacios destinados para laboratorios de cómputo, 
                así como acabados en madera que les hacen lucir acorde a las expectativas de desarrollo de la Unidad.</p>
            
            <p>La construcción del edificio de laboratorios inició el 10 de mayo de 1998, y quedó terminado durante el 2000. Este edificio de laboratorios, 
                ocupa una superficie de mil 852 metros cuadrados y consta de planta baja y dos pisos. La obra terminada tiene un total de 4 mil 856 metros cuadrados.</p>
